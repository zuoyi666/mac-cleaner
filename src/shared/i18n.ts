export type I18nKey = keyof (typeof catalog)['zh-CN']

export function resolveLanguage(input?: string | null): AppLanguage {
  if (input === 'zh-CN' || input === 'en-US') return input
  if (input?.toLowerCase().startsWith('zh')) return 'zh-CN'
  return 'en-US'
}

export function t(language: AppLanguage | undefined, key: string | undefined, params: I18nParams = {}): string {
  if (!key) return ''
  const resolvedLanguage = language ?? 'zh-CN'
  const template =
    catalog[resolvedLanguage][key as I18nKey] ?? catalog['zh-CN'][key as I18nKey] ?? catalog['en-US'][key as I18nKey] ?? key
  return template.replace(/\{(\w+)\}/g, (_match, paramName: string) => String(params[paramName] ?? `{${paramName}}`))
}
